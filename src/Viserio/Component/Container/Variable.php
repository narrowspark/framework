namespace Viserio\Component\Container;

class Variable
{
    private string $name;

    /**
     * Create a new variable instance.
     *
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * Get variable name.
     *
     * @return string
     */
    public function __toString(): string
    {
        return $this->name;
    }
}
